/**
 * Returns a greeting message for the given name.
 *
 * @param {string} name - The name to greet
 * @returns {string} Greeting message
 */
function sayHello(name) {
  return `Hello, ${name}!`;
}

// Example usage
const userName = "GitHubTest";
console.log(sayHello(userName));
